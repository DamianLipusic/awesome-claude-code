import React, { useState } from 'react';
import { View, Text, ScrollView, TouchableOpacity, StyleSheet, Alert, ActivityIndicator } from 'react-native';
import * as Print from 'expo-print';
import * as Sharing from 'expo-sharing';
import { PeptideResults, Modifications } from '../types';
import { COLORS, SPACING, FONT_SIZE, RADIUS, SHADOW } from '../constants/theme';

interface Props {
  results: PeptideResults | null;
  modifications: Modifications;
  dark: boolean;
}

function buildHTML(results: PeptideResults, mods: Modifications): string {
  const modList = [
    mods.nAcetyl ? 'N-Acetyl' : '',
    mods.cAmide  ? 'C-Amide'  : '',
    mods.disulfide > 0 ? `${mods.disulfide} Disulfide bridge(s)` : '',
  ].filter(Boolean).join(', ') || 'None';

  const rows = [
    ['Sequence Length',    `${results.length} aa`],
    ['Mol. Weight (avg)',  `${results.mwAvg.toFixed(3)} Da`],
    ['Mol. Weight (mono)', `${results.mwMono.toFixed(4)} Da`],
    ['Isoelectric Point',  `${results.pI.toFixed(2)}`],
    ['Net Charge (pH 7.4)',`${results.netCharge}`],
    ['Extinction Coeff.',  `${results.extinctionCoeff} M⁻¹cm⁻¹`],
    ['GRAVY Score',        `${results.gravy}`],
    ['Instability Index',  `${results.instabilityIndex} (${results.instabilityIndex < 40 ? 'Stable' : 'Unstable'})`],
    ['Aliphatic Index',    `${results.aliphaticIndex}`],
    ['Half-Life (N-end)',  `${results.halfLife}`],
    ['HPLC Retention Time',`${results.retentionTime}`],
    ['Solubility Score',   `${results.solubilityScore}/100`],
  ];

  const compRows = Object.entries(results.composition)
    .sort((a, b) => b[1] - a[1])
    .map(([aa, n]) => `<tr><td>${aa}</td><td>${n}</td><td>${((n/results.length)*100).toFixed(1)}%</td></tr>`)
    .join('');

  return `<!DOCTYPE html>
<html>
<head>
<meta charset="UTF-8">
<style>
  body { font-family: -apple-system, Helvetica, sans-serif; margin: 40px; color: #0f172a; }
  h1 { color: #0ea5e9; font-size: 24px; margin-bottom: 4px; }
  .sub { color: #64748b; font-size: 12px; margin-bottom: 32px; }
  .seq { font-family: monospace; background: #f1f5f9; padding: 12px; border-radius: 8px;
         word-break: break-all; font-size: 13px; margin-bottom: 32px; border-left: 4px solid #0ea5e9; }
  table { width: 100%; border-collapse: collapse; margin-bottom: 32px; }
  th { background: #0ea5e9; color: #fff; padding: 10px 12px; text-align: left; font-size: 12px; }
  td { padding: 8px 12px; font-size: 13px; border-bottom: 1px solid #e2e8f0; }
  tr:nth-child(even) td { background: #f8fafc; }
  .section { font-size: 16px; font-weight: 700; color: #0f172a; margin: 24px 0 8px; border-bottom: 2px solid #e2e8f0; padding-bottom: 4px; }
  .footer { color: #94a3b8; font-size: 11px; margin-top: 40px; text-align: center; }
  .badge { display: inline-block; padding: 2px 8px; border-radius: 99px; font-size: 11px; font-weight: 700; }
  .stable { background: #dcfce7; color: #166534; }
  .unstable { background: #fee2e2; color: #991b1b; }
</style>
</head>
<body>
  <h1>PeptiCalc Pro — Analysis Report</h1>
  <div class="sub">Generated ${new Date().toLocaleString()} · Modifications: ${modList}</div>

  <div class="section">Sequence</div>
  <div class="seq">${results.sequence}</div>

  <div class="section">Physicochemical Properties</div>
  <table>
    <thead><tr><th>Property</th><th>Value</th></tr></thead>
    <tbody>${rows.map(([k, v]) => `<tr><td>${k}</td><td>${v}</td></tr>`).join('')}</tbody>
  </table>

  <div class="section">Amino Acid Composition</div>
  <table>
    <thead><tr><th>AA</th><th>Count</th><th>%</th></tr></thead>
    <tbody>${compRows}</tbody>
  </table>

  <div class="footer">Generated by PeptiCalc Pro · For research use only</div>
</body>
</html>`;
}

export default function ExportScreen({ results, modifications, dark }: Props) {
  const [loading, setLoading] = useState(false);

  const bg     = dark ? COLORS.bgDark    : COLORS.bgLight;
  const card   = dark ? COLORS.cardDark  : COLORS.cardLight;
  const text   = dark ? COLORS.textDark  : COLORS.textLight;
  const muted  = dark ? COLORS.mutedDark : COLORS.mutedLight;
  const border = dark ? COLORS.borderDark: COLORS.borderLight;

  const exportPDF = async () => {
    if (!results) return;
    setLoading(true);
    try {
      const html = buildHTML(results, modifications);
      const { uri } = await Print.printToFileAsync({ html });
      if (await Sharing.isAvailableAsync()) {
        await Sharing.shareAsync(uri, { mimeType: 'application/pdf', dialogTitle: 'PeptiCalc Pro Report' });
      } else {
        Alert.alert('Saved', `PDF saved to: ${uri}`);
      }
    } catch (e) {
      Alert.alert('Error', 'Could not generate PDF.');
    }
    setLoading(false);
  };

  if (!results) {
    return (
      <View style={[styles.empty, { backgroundColor: bg }]}>
        <Text style={styles.emptyIcon}>📄</Text>
        <Text style={[styles.emptyTitle, { color: text }]}>No Results Yet</Text>
        <Text style={[styles.emptyText, { color: muted }]}>
          Enter a peptide sequence in the Calculator tab to generate a report.
        </Text>
      </View>
    );
  }

  return (
    <ScrollView style={[styles.screen, { backgroundColor: bg }]} contentContainerStyle={styles.content}>
      {/* Preview card */}
      <View style={[styles.card, SHADOW.card, { backgroundColor: card, borderColor: border }]}>
        <Text style={[styles.reportTitle, { color: COLORS.primary }]}>PeptiCalc Pro — Analysis Report</Text>
        <Text style={[styles.date, { color: muted }]}>{new Date().toLocaleDateString()}</Text>

        <View style={[styles.seqBox, { backgroundColor: dark ? COLORS.surfaceDark : COLORS.surfaceLight }]}>
          <Text style={[styles.seqText, { color: text }]}>{results.sequence}</Text>
        </View>

        {[
          ['Length',          `${results.length} aa`],
          ['MW (avg)',        `${results.mwAvg.toFixed(3)} Da`],
          ['MW (mono)',       `${results.mwMono.toFixed(4)} Da`],
          ['pI',              `${results.pI.toFixed(2)}`],
          ['Net Charge',      `${results.netCharge} (pH 7.4)`],
          ['ε₂₈₀',           `${results.extinctionCoeff} M⁻¹cm⁻¹`],
          ['GRAVY',           `${results.gravy}`],
          ['Instability',     `${results.instabilityIndex}`],
          ['Aliphatic',       `${results.aliphaticIndex}`],
          ['Half-Life',       `${results.halfLife}`],
          ['HPLC RT',         `${results.retentionTime}`],
          ['Solubility',      `${results.solubilityScore}/100`],
        ].map(([k, v], i) => (
          <View key={k} style={[styles.row, i % 2 === 0
            ? { backgroundColor: dark ? COLORS.surfaceDark : COLORS.surfaceLight }
            : {}]}>
            <Text style={[styles.rowKey, { color: muted }]}>{k}</Text>
            <Text style={[styles.rowVal, { color: text }]}>{v}</Text>
          </View>
        ))}
      </View>

      {/* Export button */}
      <TouchableOpacity
        style={[styles.exportBtn, loading && { opacity: 0.7 }]}
        onPress={exportPDF}
        disabled={loading}
      >
        {loading
          ? <ActivityIndicator color="#fff" />
          : <Text style={styles.exportBtnText}>Export PDF Report</Text>
        }
      </TouchableOpacity>
    </ScrollView>
  );
}

const styles = StyleSheet.create({
  screen: { flex: 1 },
  content: { padding: SPACING.md, paddingBottom: 80 },
  card: { borderRadius: RADIUS.lg, borderWidth: 1, padding: SPACING.md, marginBottom: SPACING.md },
  reportTitle: { fontSize: FONT_SIZE.lg, fontWeight: '700', marginBottom: 4 },
  date: { fontSize: FONT_SIZE.xs, marginBottom: SPACING.md },
  seqBox: { borderRadius: RADIUS.md, padding: SPACING.sm, marginBottom: SPACING.md },
  seqText: { fontFamily: 'monospace', fontSize: FONT_SIZE.sm },
  row: { flexDirection: 'row', justifyContent: 'space-between', paddingVertical: SPACING.xs, paddingHorizontal: SPACING.sm, borderRadius: 4 },
  rowKey: { fontSize: FONT_SIZE.sm, fontWeight: '600' },
  rowVal: { fontSize: FONT_SIZE.sm, fontWeight: '500' },
  exportBtn: { backgroundColor: COLORS.primary, borderRadius: RADIUS.lg, padding: SPACING.md + 4, alignItems: 'center' },
  exportBtnText: { color: '#fff', fontSize: FONT_SIZE.lg, fontWeight: '700' },
  empty: { flex: 1, alignItems: 'center', justifyContent: 'center' },
  emptyIcon: { fontSize: 48, marginBottom: SPACING.md },
  emptyTitle: { fontSize: FONT_SIZE.xl, fontWeight: '700', marginBottom: SPACING.sm },
  emptyText: { fontSize: FONT_SIZE.sm, textAlign: 'center', lineHeight: 20, maxWidth: 280 },
});
